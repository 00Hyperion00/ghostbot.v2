                                         'async def fetch_fresh_exchange_balance_source(engine: Any, settings: Any, *, '
                                         'base_asset: str, quote_asset: str, dust_floor: float) -> dict[str, Any]:\n'
                                         '    attempts: list[dict[str, Any]] = []\n'
                                         '    method_names = (\n'
                                         '        "fetch_balance", "fetch_balances", "get_balances", '
                                         '"get_account_balances",\n'
                                         '        "fetch_account_balances", "get_account", "account", '
                                         '"get_account_info",\n'
                                         '        "get_spot_account", "spot_account", "account_info", '
                                         '"fetch_account",\n'
                                         '    )\n'
                                         '    for obj in _object_candidates(engine):\n'
                                         '        obj_name = obj.__class__.__name__\n'
                                         '        for method_name in method_names:\n'
                                         '            method = getattr(obj, method_name, None)\n'
                                         '            if not callable(method):\n'
                                         '                continue\n'
                                         '            if not _callable_without_required_args(method):\n'
                                         '                attempts.append({"object": obj_name, "method": method_name, '
                                         '"called": False, "reason": "required_arguments_present"})\n'
                                         '                continue\n'
                                         '            try:\n'
                                         '                raw = await _maybe_await(method())\n'
                                         '                normalized = _normalize_fresh_exchange_balance(raw, '
                                         'base_asset=base_asset, quote_asset=quote_asset, dust_floor=dust_floor)\n'
                                         '                attempts.append({"object": obj_name, "method": method_name, '
                                         '"called": True, "ok": True})\n'
                                         '                return {\n'
                                         '                    "ok": True,\n'
                                         '                    "source_object": obj_name,\n'
