def main_for_patch(patch_id: str, argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--reports-dir", default="reports/recovery")
    parser.add_argument("--once-json", action="store_true")
    parser.add_argument("--write-report", action="store_true")
    args = parser.parse_args(argv)
    payload = evaluate_phase(patch_id, reports_dir=args.reports_dir, write_reports=args.write_report)
