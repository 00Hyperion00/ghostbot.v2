def utc_now_compact() -> str:
    from datetime import datetime, timezone
    return datetime.now(timezone.utc).strftime("%Y%m%dT%H%M%SZ")


def sha256_json(payload: Mapping[str, Any]) -> str:
    blob = json.dumps(payload, sort_keys=True, separators=(",", ":"), ensure_ascii=False).encode("utf-8")
    return hashlib.sha256(blob).hexdigest()


def _is_ready_source(payload: Mapping[str, Any] | None, patch_id: str, patch_version: str) -> bool:
    if not payload:
        return False
    return (
        payload.get("ok") is True
        and payload.get("status") == "READY"
        and str(payload.get("patch_id")) == patch_id
        and str(payload.get("patch_version")) == patch_version
        and int(payload.get("final_safety_violation_count", 0)) == 0
        and payload.get("approved_for_live_real", False) is False
        and payload.get("approved_for_exchange_submit", False) is False
        and payload.get("exchange_submit_performed", False) is False
        and payload.get("network_order_submit_performed", False) is False
        and payload.get("paper_order_submit_performed", False) is False
    )


def _load_json(path: Path) -> dict[str, Any] | None:
    try:
        data = json.loads(path.read_text(encoding="utf-8"))
    except Exception:
        return None
    return data if isinstance(data, dict) else None


def _find_external_source_report(reports_dir: str | Path, patch_id: str, patch_version: str) -> tuple[dict[str, Any] | None, Path | None]:
    root = Path(reports_dir)
    if not root.exists():
        return None, None
    candidates = sorted(root.glob(f"{patch_id}*ready.json"), key=lambda p: p.stat().st_mtime, reverse=True)
    for path in candidates:
        payload = _load_json(path)
        if _is_ready_source(payload, patch_id, patch_version):
            return payload, path
    for path in sorted(root.glob("*.json"), key=lambda p: p.stat().st_mtime, reverse=True):
        payload = _load_json(path)
        if _is_ready_source(payload, patch_id, patch_version):
            return payload, path
    return None, None


def _rules(spec: Mapping[str, Any]) -> list[str]:
    rules = spec.get("rules", [])
    return list(rules) if isinstance(rules, list) else []


def _source_for_spec(spec: Mapping[str, Any], reports_dir: str | Path, stack: tuple[str, ...]) -> tuple[dict[str, Any] | None, Path | None, bool]:
    source_patch_id = str(spec["source_patch_id"])
    source_version = str(spec["source_version"])
    if source_patch_id in PHASE_BY_ID:
        if source_patch_id in stack:
            return None, None, False
        payload = evaluate_phase(source_patch_id, reports_dir=reports_dir, write_reports=False, _stack=stack + (source_patch_id,))
        return payload, None, _is_ready_source(payload, source_patch_id, source_version)
    payload, path = _find_external_source_report(reports_dir, source_patch_id, source_version)
    return payload, path, payload is not None


def _phase_group_key(phase: int) -> str:
    if phase == 45:
        return "actual_evidence_intake"
    if phase == 46:
        return "actual_no_order_soak_acceptance"
    if phase == 47:
        return "paper_order_path_authorization_review"
    if phase == 48:
        return "paper_order_dry_run_contract"
    return "controlled_paper_submit_enablement_review"


