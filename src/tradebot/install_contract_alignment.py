def evaluate_install_contract_alignment(repo_root: Path, reports_dir: Path, *, write_reports: bool = False) -> dict[str, Any]:
    repo_root = repo_root.resolve()
    reports_dir = reports_dir.resolve()
    errors: list[str] = []
    source_path = latest_report(reports_dir, SOURCE_37A_PATTERN)
    source_37a: dict[str, Any] = {}
    if source_path is None:
        errors.append(f"missing_source_37a_report:{SOURCE_37A_PATTERN}")
    else:
        try:
            source_37a = load_json(source_path)
        except Exception as exc:  # pragma: no cover
            errors.append(f"invalid_source_37a_report:{source_path}:{exc}")

    source_safety_violations = truthy_violations(source_37a, NO_SUBMIT_FALSE_FLAGS) if source_37a else []
    source_37a_complete = (
        bool(source_37a)
        and source_37a.get("status") == "READY"
        and source_37a.get("decision") == "POST_PHASE_36_PRODUCTION_READINESS_REBASELINE_READY_NO_SUBMIT_37A_PLANNING_GATE_LOCKED"
        and source_37a.get("source_36g_complete") is True
        and source_37a.get("phase_36_final_closed") is True
        and source_37a.get("phase_37_planning_only") is True
        and source_37a.get("phase_37_unlocked") is False
        and source_37a.get("production_readiness_rebaseline_ready") is True
        and source_37a.get("p0_hardening_gap_matrix_complete") is True
        and int(source_37a.get("p0_hardening_open_gap_count", 0)) >= 10
        and len(source_safety_violations) == 0
    )

    git_state = read_git_state(repo_root)
    install_contract = audit_install_contract(repo_root)
    p0_delta = build_p0_gap_closure_delta(install_contract)
    gate = build_no_submit_p0_1_hardening_gate(install_contract, p0_delta)

    ready = (
        source_37a_complete
        and bool(install_contract.get("install_contract_alignment_complete"))
        and bool(p0_delta.get("p0_gap_closure_delta_complete"))
        and bool(gate.get("no_submit_p0_1_hardening_gate_complete"))
        and not errors
    )
    status = "READY" if ready else "NOT_READY"
    decision = READY_DECISION if ready else NOT_READY_DECISION
    stamp = utc_stamp()

    result: dict[str, Any] = {
        "ok": ready,
        "status": status,
        "decision": decision,
        "errors": errors,
        "patch_id": PATCH_ID,
        "patch_name": PATCH_NAME,
        "patch_version": PATCH_VERSION,
        "check_name": CHECK_NAME,
        "git_available": git_state.git_available,
        "git_branch": git_state.git_branch,
        "git_head_short": git_state.git_head_short,
        "phase_37_tags_observed": list(git_state.local_phase_37_tags),
        "phase_37_tag_count_observed": len(git_state.local_phase_37_tags),
        "source_37a_complete": source_37a_complete,
        "source_37a_status": "SOURCE_37A_READY" if source_37a_complete else "SOURCE_37A_NOT_READY",
        "source_37a_report": str(source_path) if source_path else None,
        "source_37a_decision": source_37a.get("decision"),
        "source_37a_safety_violation_count": len(source_safety_violations),
        "source_37a_safety_violations": source_safety_violations,
        "source_37a_phase_36_final_closed": source_37a.get("phase_36_final_closed"),
        "source_37a_phase_37_planning_only": source_37a.get("phase_37_planning_only"),
        "source_37a_p0_open_gap_count": source_37a.get("p0_hardening_open_gap_count"),
        "phase_34_closed": True,
        "phase_35_closed": True,
        "phase_36_final_closed": bool(source_37a.get("phase_36_final_closed", False)) if source_37a else False,
        "phase_37_planning_only": True,
        "phase_37_unlocked": False,
        "phase_37_execution_started": False,
        "production_hardening_p0_1_ready": ready,
        "production_hardening_p0_1_scope": "install_contract_alignment_only",
        "production_readiness_status": "P0_1_INSTALL_CONTRACT_ALIGNMENT_READY_NO_SUBMIT" if ready else "P0_1_INSTALL_CONTRACT_ALIGNMENT_NOT_READY_NO_SUBMIT",
        "paper_transition_status": "PAPER_TRANSITION_BLOCKED_37B_INSTALL_CONTRACT_ALIGNMENT_NO_SUBMIT",
        "next_phase": NEXT_PHASE,
        "accepted_for_install_contract_alignment": ready,
        "install_contract_alignment_path": None,
        "p0_gap_closure_delta_path": None,
        "no_submit_p0_1_hardening_gate_path": None,
        "report_path": None,
    }
    result.update(install_contract)
    result.update(p0_delta)
    result.update(gate)

    for flag in NO_SUBMIT_FALSE_FLAGS:
        result[flag] = False
    result["paper_transition_blocked"] = True
    result["paper_transition_ready"] = False
    result["paper_transition_unblocked"] = False
    result["paper_transition_approval_performed"] = False
    result["paper_environment_enabled"] = False
    result["live_environment_enabled"] = False
    result["network_request_allowed_now"] = False
    result["exchange_submit_allowed"] = False
    result["network_submit_allowed"] = False
    result["paper_submit_allowed"] = False
    result["live_real_submit_allowed"] = False
    result["runtime_overlay_allowed"] = False
    result["order_submit_performed"] = False
    result["exchange_submit_performed"] = False
    result["trading_action_performed"] = False
    result["training_performed"] = False
    result["reload_performed"] = False
    result["runtime_overlay_activated"] = False
    result["install_contract_mutation_performed"] = False
    result["requirements_alignment_mutation_performed"] = False
    result["readme_install_contract_mutation_performed"] = False
    result["launcher_install_contract_mutation_performed"] = False

    if write_reports:
        reports_dir.mkdir(parents=True, exist_ok=True)
        install_path = reports_dir / f"{PATCH_ID}_install_contract_alignment_{stamp}.json"
        delta_path = reports_dir / f"{PATCH_ID}_p0_gap_closure_delta_{stamp}.json"
        gate_path = reports_dir / f"{PATCH_ID}_no_submit_p0_1_hardening_gate_{stamp}.json"
        report_path = reports_dir / f"{PATCH_ID}_install_contract_alignment_{stamp}_{status.lower()}.json"
        write_json(install_path, install_contract)
        write_json(delta_path, p0_delta)
        write_json(gate_path, gate)
        result["install_contract_alignment_path"] = str(install_path)
        result["p0_gap_closure_delta_path"] = str(delta_path)
        result["no_submit_p0_1_hardening_gate_path"] = str(gate_path)
        result["report_path"] = str(report_path)
        write_json(report_path, result)
    return result


def build_arg_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description=f"{PATCH_VERSION} {PATCH_NAME}")
    parser.add_argument("--repo-root", default=".")
    parser.add_argument("--reports-dir", default="reports/recovery")
    parser.add_argument("--once-json", action="store_true")
    parser.add_argument("--write-reports", action="store_true")
    return parser


def main(argv: Sequence[str] | None = None) -> int:
    args = build_arg_parser().parse_args(argv)
    result = evaluate_install_contract_alignment(
        repo_root=Path(args.repo_root),
        reports_dir=Path(args.reports_dir),
        write_reports=bool(args.write_reports),
    )
    print(json.dumps(result, ensure_ascii=False, sort_keys=True))
    return 0 if result.get("ok") else 1


