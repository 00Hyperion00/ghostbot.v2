def upsert_readme_block(text: str) -> tuple[str, bool]:
    block = readme_install_contract_block()
    if README_MARKER_START in text and README_MARKER_END in text:
        pattern = re.compile(re.escape(README_MARKER_START) + r".*?" + re.escape(README_MARKER_END), re.DOTALL)
        new_text = pattern.sub(block.strip(), text)
        if not new_text.endswith("\n"):
            new_text += "\n"
        return new_text, new_text != text
    separator = "\n" if text.endswith("\n") else "\n\n"
    return text + separator + block, True


def patch_launcher_text(path: Path, text: str) -> tuple[str, bool]:
    original = text
    replacements = [
        ("pip install -r requirements.txt", "python -m pip install -r requirements.txt"),
        ("pip3 install -r requirements.txt", "python -m pip install -r requirements.txt"),
        ("py -m pip install -r requirements.txt", "python -m pip install -r requirements.txt"),
    ]
    for old, new in replacements:
        text = text.replace(old, new)
    if "4B436637B install contract" not in text:
        comment = "REM 4B436637B install contract: requirements.txt is generated from pyproject.toml\n" if path.suffix.lower() == ".bat" else "# 4B436637B install contract: requirements.txt is generated from pyproject.toml\n"
        text = comment + text
    return text, text != original


def backup_file(path: Path, backup_root: Path, repo_root: Path) -> Path:
    try:
        relative_path = path.resolve().relative_to(repo_root.resolve())
    except ValueError:
        relative_path = Path(path.name)
    backup_path = backup_root / relative_path
    backup_path.parent.mkdir(parents=True, exist_ok=True)
    shutil.copy2(path, backup_path)
    return backup_path


