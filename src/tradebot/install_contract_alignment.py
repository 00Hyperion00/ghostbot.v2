def utc_stamp() -> str:
    return datetime.now(timezone.utc).strftime("%Y%m%dT%H%M%SZ")


def stable_digest(payload: Mapping[str, Any] | Sequence[Any]) -> str:
    encoded = json.dumps(payload, sort_keys=True, ensure_ascii=False, separators=(",", ":")).encode("utf-8")
    return hashlib.sha256(encoded).hexdigest()


def load_json(path: Path) -> dict[str, Any]:
    with path.open("r", encoding="utf-8") as handle:
        data = json.load(handle)
    if not isinstance(data, dict):
        raise ValueError(f"JSON root is not an object: {path}")
    return data


def write_json(path: Path, payload: Mapping[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as handle:
        json.dump(payload, handle, ensure_ascii=False, indent=2, sort_keys=True)
        handle.write("\n")


def latest_report(reports_dir: Path, pattern: str) -> Path | None:
    if not reports_dir.exists():
        return None
    matches = [path for path in reports_dir.glob(pattern) if path.is_file()]
    if not matches:
        return None
    return max(matches, key=lambda path: (path.stat().st_mtime_ns, path.name))


def truthy_violations(source: Mapping[str, Any], flags: Iterable[str]) -> list[str]:
    return [name for name in flags if bool(source.get(name, False))]


@dataclass(frozen=True)
class GitState:
    git_available: bool
    git_branch: str | None
    git_head_short: str | None
    local_phase_37_tags: tuple[str, ...]


def _run_git(args: Sequence[str], cwd: Path, timeout: int = 20) -> subprocess.CompletedProcess[str]:
    return subprocess.run(
        ["git", *args],
        cwd=str(cwd),
        text=True,
        capture_output=True,
        timeout=timeout,
        check=False,
    )


def read_git_state(repo_root: Path) -> GitState:
    try:
        branch_result = _run_git(["rev-parse", "--abbrev-ref", "HEAD"], repo_root)
        head_result = _run_git(["rev-parse", "--short", "HEAD"], repo_root)
        phase37_result = _run_git(["tag", "--list", "4B.4.3.6.6.37*"], repo_root)
    except (FileNotFoundError, subprocess.TimeoutExpired):
        return GitState(False, None, None, tuple())

    git_available = branch_result.returncode == 0 and head_result.returncode == 0
    branch = branch_result.stdout.strip() if branch_result.returncode == 0 else None
    head = head_result.stdout.strip() if head_result.returncode == 0 else None
    phase37_tags = tuple(sorted(line.strip() for line in phase37_result.stdout.splitlines() if line.strip())) if phase37_result.returncode == 0 else tuple()
    return GitState(git_available, branch, head, phase37_tags)


def _strip_inline_comment(line: str) -> str:
    in_quote: str | None = None
    escaped = False
    result: list[str] = []
    for char in line:
        if escaped:
            result.append(char)
            escaped = False
            continue
        if char == "\\":
            result.append(char)
            escaped = True
            continue
        if char in {"'", '"'}:
            if in_quote == char:
                in_quote = None
            elif in_quote is None:
                in_quote = char
            result.append(char)
            continue
        if char == "#" and in_quote is None:
            break
        result.append(char)
    return "".join(result).strip()


def _parse_toml_string_array(raw: str) -> list[str]:
    values: list[str] = []
    for match in re.finditer(r"(['\"])(.*?)\1", raw, flags=re.DOTALL):
        value = match.group(2).strip()
        if value:
            values.append(value)
    return values


def _extract_project_dependencies_with_tomllib(pyproject_path: Path) -> list[str] | None:
    try:
        import tomllib  # type: ignore[import-not-found]
    except ModuleNotFoundError:  # Python 3.10 fallback below.
        return None
    try:
        data = tomllib.loads(pyproject_path.read_text(encoding="utf-8"))
    except Exception:
        return None
    project = data.get("project")
    if not isinstance(project, dict):
        return []
    dependencies = project.get("dependencies", [])
    if not isinstance(dependencies, list):
        return []
    return [str(item).strip() for item in dependencies if str(item).strip()]


def extract_project_dependencies(pyproject_path: Path) -> list[str]:
    if not pyproject_path.exists():
        return []
    parsed = _extract_project_dependencies_with_tomllib(pyproject_path)
    if parsed is not None:
        return sorted(dict.fromkeys(parsed), key=str.lower)

    text = pyproject_path.read_text(encoding="utf-8")
    project_match = re.search(r"(?ms)^\[project\]\s*(.*?)(?=^\[[^\]]+\]|\Z)", text)
    if not project_match:
        return []
    project_body = project_match.group(1)
    dep_match = re.search(r"(?ms)^dependencies\s*=\s*\[(.*?)\]", project_body)
    if not dep_match:
        return []
    return sorted(dict.fromkeys(_parse_toml_string_array(dep_match.group(1))), key=str.lower)


def normalize_requirement_name(requirement: str) -> str:
    req = requirement.strip()
    if not req:
        return ""
    if req.startswith("-"):
        return req.lower()
    req = req.split(";", 1)[0].strip()
    req = re.split(r"\s*(?:==|>=|<=|~=|!=|>|<|===)\s*", req, maxsplit=1)[0].strip()
    req = req.split("[", 1)[0].strip()
    return re.sub(r"[-_.]+", "-", req).lower()


def parse_requirements(path: Path) -> list[str]:
    if not path.exists():
        return []
    items: list[str] = []
    for raw_line in path.read_text(encoding="utf-8").splitlines():
        line = _strip_inline_comment(raw_line)
        if line:
            items.append(line)
    return items


def render_requirements(dependencies: Sequence[str]) -> str:
    normalized = [dep.strip() for dep in dependencies if dep.strip()]
    ordered = sorted(dict.fromkeys(normalized), key=str.lower)
    return REQUIREMENTS_HEADER + "\n".join(ordered) + ("\n" if ordered else "")


def expected_requirements_digest(dependencies: Sequence[str]) -> str:
    return hashlib.sha256(render_requirements(dependencies).encode("utf-8")).hexdigest()


def requirements_alignment(pyproject_dependencies: Sequence[str], requirements_items: Sequence[str]) -> dict[str, Any]:
    py_by_name = {normalize_requirement_name(dep): dep for dep in pyproject_dependencies if normalize_requirement_name(dep)}
    req_by_name = {normalize_requirement_name(req): req for req in requirements_items if normalize_requirement_name(req)}
    missing_names = sorted(set(py_by_name) - set(req_by_name))
    extra_names = sorted(name for name in set(req_by_name) - set(py_by_name) if not name.startswith("-"))
    mismatch_count = len(missing_names) + len(extra_names)
    return {
        "pyproject_dependency_count": len(pyproject_dependencies),
        "requirements_dependency_count": len(requirements_items),
        "requirements_missing_from_pyproject_dependency_names": missing_names,
        "requirements_extra_dependency_names": extra_names,
        "requirements_pyproject_mismatch_count": mismatch_count,
        "requirements_pyproject_aligned": mismatch_count == 0 and len(pyproject_dependencies) > 0,
    }


